/*
Audiorenamer traverses a directory and renames music tracks based on their
metadata.

# Usage

	$ audiorenamer [flags...] <dir>

The new filename is generated by executing a Go template against the file's
metadata (see https:pkg.go.dev/github.com/dhowden/tag#Metadata for
available fields). 'track' template function is provided for convenience,
returning only the track number from the metadata.

If the template results in a filename containing slashes, these are removed.
*/
